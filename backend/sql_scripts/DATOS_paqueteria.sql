-- Consultas:

-- 1. Listar todos los empleados y sus cargos:
SELECT P.NOMBRE_PERSONA, C.NOMBRE_CARGO
FROM EMPLEADO E
JOIN PERSONA P ON E.ID_PERSONA = P.ID_PERSONA
JOIN CARGO C ON E.ID_CARGO = C.ID_CARGO;

-- 2. Mostrar las facturas pendientes de pago:
SELECT F.* FROM FACTURA F
JOIN PAQUETE P ON F.ID_PAQUETE = P.ID_PAQUETE
WHERE F.ESTADOPAGO = 0;

-- 3. Mostrar la cantidad de paquetes enviados desde cada centro de distribucion:
SELECT CD.UBICACION_CEN_DIST, COUNT(*) AS CANTIDAD_PAQUETES FROM PAQUETE P
JOIN CENTRO_DISTRIBUCION CD ON P.ID_CENTRO_DISTRIBUCION = CD.ID_CENTRO_DISTRIBUCION
GROUP BY CD.UBICACION_CEN_DIST;

-- 4. Mostrar los mensajeros y los vehiculos que manejan:
SELECT 
    e.ID_EMPLEADO,
    per.NOMBRE_PERSONA AS NOMBRE_EMPLEADO,
    car.NOMBRE_CARGO,
    v.TIPO AS VEHICULO,
    CASE 
        WHEN v.ESTADO_VEHICULO = 0 THEN 'Disponible'
        WHEN v.ESTADO_VEHICULO = 1 THEN 'En mantenimiento'
        ELSE 'Fuera de servicio'
    END AS ESTADO_VEHICULO,
    cd.UBICACION_CEN_DIST AS CENTRO_ASIGNADO
FROM 
    EMPLEADO e
JOIN 
    PERSONA per ON e.ID_PERSONA = per.ID_PERSONA
JOIN 
    CARGO car ON e.ID_CARGO = car.ID_CARGO
JOIN 
    EMPLEADO_VEHICULO ev ON e.ID_EMPLEADO = ev.ID_EMPLEADO
JOIN 
    VEHICULO v ON ev.ID_VEHICULO = v.ID_VEHICULO
JOIN 
    CENTRO_DISTRIBUCION cd ON v.ID_CENTRO_DISTRIBUCION = cd.ID_CENTRO_DISTRIBUCION
WHERE 
    car.NOMBRE_CARGO = 'Mensajero'
ORDER BY 
    e.ID_EMPLEADO;
    
